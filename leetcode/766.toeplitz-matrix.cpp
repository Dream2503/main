// @lc code=start
class Solution {
public:
    bool isToeplitzMatrix(const std::vector<std::vector<int>>& matrix) {
        const int m = matrix.size(), n = matrix[0].size();

        for (int i = 0; i < m - 1; i++) {
            for (int j = 0; j < n - 1; j++) {
                if (matrix[i][j] != matrix[i + 1][j + 1]) {
                    return false;
                }
            }
        }
        return true;
    }
};
// @lc code=end
